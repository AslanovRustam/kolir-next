export default function Services({ content: c }: { content: ServicesContent }) {
  const SVC = c.cards
  const secRef = useRef<HTMLElement>(null)

  // мобільний слайдер: синхронізуємо крапки-пагінацію зі скролом
  useEffect(() => {
    const sec = secRef.current
    if (!sec) return
    const grid = sec.querySelector<HTMLElement>('.services-grid')
    const dotsWrap = sec.querySelector<HTMLElement>('.services-dots')
    if (!grid || !dotsWrap) return
    const slides = Array.from(grid.querySelectorAll<HTMLElement>('.svc'))
    const dots = Array.from(dotsWrap.querySelectorAll<HTMLElement>('span'))
    if (!slides.length || !dots.length) return

    let raf = 0
    const update = () => {
      const center = grid.scrollLeft + grid.clientWidth / 2
      let idx = 0,
        best = Infinity
      slides.forEach((s, i) => {
        const c = s.offsetLeft + s.offsetWidth / 2
        const d = Math.abs(c - center)
        if (d < best) {
          best = d
          idx = i
        }
      })
      dots.forEach((d, i) => d.classList.toggle('is-active', i === idx))
    }
    const onScroll = () => {
      if (raf) cancelAnimationFrame(raf)
      raf = requestAnimationFrame(update)
    }
    grid.addEventListener('scroll', onScroll, { passive: true })
    const dotClicks = dots.map((d, i) => {
      const fn = () => {
        const s = slides[i]
        grid.scrollTo({ left: s.offsetLeft - (grid.clientWidth - s.offsetWidth) / 2, behavior: 'smooth' })
      }
      d.addEventListener('click', fn)
      return fn
    })
    update()
    return () => {
      grid.removeEventListener('scroll', onScroll)
      dots.forEach((d, i) => d.removeEventListener('click', dotClicks[i]))
    }
  }, [])

  return (
    <section className="services" id="Services" aria-label="What We Do" ref={secRef}>
      <div className="services-card">
        <div className="services-clip" aria-hidden="true">
          <div className="services-bg">
            <svg className="s-scribble vector-s1" width="692" height="612" viewBox="0 0 692 612" fill="currentColor" focusable="false">
              <path d={S1} fill="currentColor" />
            </svg>
            <svg className="s-scribble vector-s2" width="498" height="904" viewBox="0 0 498 904" fill="currentColor" focusable="false">
              <path d={S2} fill="currentColor" />
            </svg>
          </div>
        </div>

        <div className="services-content">
          <div className="services-head" data-reveal="up">
            <h2 className="services-title">{c.title}</h2>
          </div>

          <svg aria-hidden="true" focusable="false" style={{ position: 'absolute', width: 0, height: 0, overflow: 'hidden' }}>
            <symbol id="svcCorner" viewBox="0 0 414 388">
              <path d={CORNER} fill="currentColor" />
            </symbol>
          </svg>

          <div className="services-grid" data-reveal-stagger>
            {SVC.map((s, i) => (
              <a className="svc" key={i}>
                <svg className="svc-corner" aria-hidden="true" focusable="false">
                  <use href="#svcCorner" />
                </svg>
                <div className="svc-text">
                  <h3 className="svc-title">{s.title}</h3>
                  <span className="svc-line" aria-hidden="true" />
                  <p className="svc-desc">{s.desc}</p>
                </div>
                <span className="svc-ico" aria-hidden="true">
                  <svg width="14" height="14" viewBox="0 0 14 14" fill="none">
                    <path d={ARROW} fill="currentColor" />
                  </svg>
                </span>
              </a>
            ))}
          </div>

          <div className="services-dots" aria-hidden="true">
            <span className="is-active" />
            <span />
            <span />
            <span />
            <span />
          </div>
        </div>
      </div>
    </section>
  )
}
