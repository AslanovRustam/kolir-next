/* ============================ Types ============================ */

export type Category =
  | 'Branding'
  | 'Web'
  | 'Product'
  | 'Motion'
  | 'Editorial';

/** A simple image object used inside `group`, `bento` and `slider` blocks. */
export interface GalleryImage {
  src: string;
  /** Tailwind grid utility classes — only present on `bento` images. */
  cls?: string;
  /** CSS aspect-ratio string (e.g. '1600/800') — only present on some `bento` images. */
  aspect?: string;
  /** group only: overlay a dark→transparent gradient on the top edge for a
   *  seamless transition from a dark block above (e.g. video → image). */
  topFade?: boolean;
}

/** group: a vertical stack of images (objects with `src`). */
export interface GroupBlock {
  type: 'group';
  images: GalleryImage[];
}

/** logos: a grid of logo images, given as bare string paths (NOT objects). */
export interface LogosBlock {
  type: 'logos';
  images: string[];
}

/** text: a copy block with an optional heading and a body. */
export interface TextBlock {
  type: 'text';
  heading?: string;
  body: string;
}

/** local: a single captioned image. */
export interface LocalBlock {
  type: 'local';
  src: string;
  caption: string;
}

/** bento: a masonry/bento grid of images, each carrying `cls` (and sometimes `aspect`). */
export interface BentoBlock {
  type: 'bento';
  caption?: string;
  images: GalleryImage[];
}

/** slider: a horizontal carousel of images (objects with `src`). */
export interface SliderBlock {
  type: 'slider';
  caption?: string;
  images: GalleryImage[];
}

/** video: a single video with a poster image. */
export interface VideoBlock {
  type: 'video';
  src: string;
  poster: string;
  caption: string;
}

export type GalleryBlock =
  | GroupBlock
  | LogosBlock
  | TextBlock
  | LocalBlock
  | BentoBlock
  | SliderBlock
  | VideoBlock;

export interface CaseLink {
  label: string;
  url: string;
  kind: 'social' | 'pdf' | 'site';
}

export interface CaseItem {
  id: string;
  title: string;
  client: string;
  year: number;
  categories: Category[];
  /** Cover background palette key (sun|cream|violet|lilac|ink). */
  color: string;
  /** Cover text colour key (ink|cream). */
  text: string;
  teaser: string;
  scope: string[];
  description: string;
  results: string[];
  gallery: GalleryBlock[];
  links: CaseLink[];
}

/** Per-gallery-index override: any of caption/heading/body may be present. */
export interface UkGalleryOverride {
  caption?: string;
  heading?: string;
  body?: string;
}

export interface UkOverride {
  title?: string;
  client?: string;
  teaser?: string;
  description?: string;
  scope?: string[];
  results?: string[];
  /** Keyed by gallery block index (as it appears in the matching case's `gallery`). */
  gallery?: Record<number, UkGalleryOverride>;
}

